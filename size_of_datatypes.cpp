#include<iostream>
using namespace  std;
int main(){
    int a;
    float b;
    char c;
    bool d;
    long int e;
    cout<<"Size of int is : "<<sizeof(a)<<endl;
    cout<<"Size of float is : "<<sizeof(b)<<endl;
    cout<<"Size of char is : "<<sizeof(c)<<endl;
    cout<<"Size of bool is : "<<sizeof(d)<<endl;
    cout<<"Size of long int is : "<<sizeof(e)<<endl;
    return 0;
}